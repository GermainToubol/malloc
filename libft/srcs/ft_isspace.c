#include <stdint.h>
#include <libft.h>

int	ft_isspace(int c)
{
	uint8_t cc;

	cc = (uint8_t)c;
	if ((cc >= '\t' && c <= '\r') || c == ' ')
		return (1);
	return (0);
}
